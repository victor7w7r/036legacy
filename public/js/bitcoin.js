
var BTCaddress="35QcBjNwr7S5f8taxb4ZPHXTY35kAXTyLq";
var LTCaddress="MUguoM2jXCVGaJB5GUygHHjj764H2NBEcN";

function popupWindow(url, title, win, w, h) 
{
    const y = win.top.outerHeight / 2 + win.top.screenY - ( h / 2);
    const x = win.top.outerWidth / 2 + win.top.screenX - ( w / 2);
    return win.open(url+BTCaddress, title, `toolbar=no, location=no, directories=no, status=no, menubar=no, scrollbars=no, resizable=no, copyhistory=no, width=${w}, height=${h}, top=${y}, left=${x}`);
}


